import MapView from "react-native-maps";
import { CardType, IOpenedCard } from "./footer";
import { getLocationByCoords } from "../scripts/api";

export const Map: React.FC<{cardSet: React.Dispatch<IOpenedCard>}> = props => {

    const handlePress = async(e) => {
        const { latitude, longitude } = e.nativeEvent.coordinate;

        const locationName = String(await getLocationByCoords(latitude, longitude));
        const card = {name:locationName, type:CardType.Location, filters:"", lat:latitude, lng:longitude}
        props.cardSet(card);
    };

    const onRegionChange = (e) => {

        //console.log(`Latitude: ${e.latitude}, Longitude: ${e.longitude}`);
      }
    return (
        <View>

            <MapView

  provider={MapView.PROVIDER_GOOGLE}
  style={{ flex: 1, width: 1080, height: 1920 }}
  initialRegion={{
    latitude: 53.90572604158633,
    longitude: -2.499390318989753, 
    latitudeDelta: 10, 
    longitudeDelta: 10, 
}}
customMapStyle={mapJson}
onRegionChange={onRegionChange}
onPress={handlePress}
/>
        </View>
    );
};

